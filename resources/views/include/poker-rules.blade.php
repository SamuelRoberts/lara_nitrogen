  <li><p><em>Omaha (Omaha Hi)</em>Omaha is a fun and exciting way to play poker with a twist. The standard rules and regulations are much the same as Hold ‘Em poker, and those who are familiar should have an easy time picking up Omaha. Hand values and ranking remain the same, as does betting and the general structure of the game. The differences are explained below.</p></li>
  <li><p><em>Omaha Hi Rules</em>

        Each player is dealt four cards at the beginning of each hand, rather than the customary two. All four of those cards are private and hidden from the other players.<br><br>
        The play structure of each hand remains the same: dealer, small and big blinds, and rounds of betting all have the same structure as outlined under Hold ‘Em rules. (Omaha games are limit games to add structure to a game where hands and combinations of cards can change rapidly)<br><br>
        The goal of Omaha is to for players to create the best hand possible by using two of the four cards from their hand and three of the five cards revealed to the table. Players must abide by this rule and not use three cards from their hand and two from those revealed on the table or any other combination.

  </p></li>
  <li><p><em>Telesina</em>Telesina is a variant of 5 Card Stud, and is played using a deck of only 32 cards rather than the traditional 52. More common in Europe it might be a fun way to spice up your poker experience!</p></li>
  <li><p><em>Telesina Rules</em>

        A maximum of six players can play telesina at once. Two cards are dealt to each player. The first card dealt to each player will be concealed while the second card is face up for everyone to see. The first betting round will then commence. The player who hold the highest card after the first deal leads the betting.<br><br>
        After the initial deal and round of betting, three more cards are dealt to each player face-up and visible to all players. A round of betting follows each deal, consistent with most varieties of poker.<br><br>
        The sixth card to be dealt (following the initial two hole cards and following three visible cards to each player) is the “Vela Card” which appears in the center of the table. Every player still in the hand is able this card. Then the final round of betting will commence.<br><br>
        Unique to Telesina is a variation in hand values. For a game of Telesina, a flush is valued more highly than a full house. A full ranking of Telesina hands follows:<br>
