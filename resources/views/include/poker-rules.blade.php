       <li><p><em>Synergy Collusion Policy</em>

        Collusion is a form of cheating where two or more players work together in a poker game to gain an advantage over one or more players at the table. This is sometimes done by a single person (using more than one account) or by multiple people (each using their own account). It can happen in cash games as well as SnGs and tournaments.<br><br>

