      <ol>
       <li><p><em>No Limit Texas Hold ‘Em (NLHE)</em>No Limit Hold ‘Em is poker as it was meant to be played, no caps or maximum bets on hands, and players go all in and all out for the win!</p></li>
       <li><p><em>NLHE Rules</em>

        Each Player is dealt two “pocket” cards. The dealer is designated by a white chip marked with a letter D.<br><br>
        The “small blind” (the player to the immediate left of the dealer) automatically contributes half of the necessary ante or “blind” to the pot. The “big blind” (the player to the immediate left of the small blind) automatically contributes the full ante or “blind” to the pot. A round of betting commences, during which each player (in clockwise order) has an opportunity to: a) match the blind (the minimum amount of mBTC necessary to participate in the hand); b) raise (bet a larger amount of money than is necessary to participate in the hand, thereby forcing other players to match that amount if they wish to continue), or c) fold (refuse to match the blinds or raises and thereby withdraw from that particular hand). Once the action circles around the table, the small blind must pay the remaining half of his/her blind as well as any additional raises in order to continue playing. If a big or small blind will not or cannot match any raises and folds, their blinds are lost to the pot.<br><br>
        When all blinds or bets have been matched by players wishing to remain in the hand, play moves on to the “flop”. The flop is the revealing of three cards in the middle of the table to all players. Following the flop, a round of betting commences with the remaining player to the nearest left of the dealer. Each player has the option to: a) check (not bet any additional mBTC and pass the option on to the next player); b) bet/raise (place into the pot an additional amount of mBTC which must be matched by any other player wishing to remain in the game), or c) fold (refuse to match any additional bets or raises and withdraw from the hand, losing all mBTC previously contributed to the pot through the course of the hand). If all players involved in a hand choose to check, the hand moves on without any additional mBTC being contributed to the pot. Otherwise, all bets/raises must be matched by players wishing to continue before the hand continues. Should all players but one choose to fold their hands and not match a bet, the player who has made that high bet wins the hand.<br><br>
        The “turn” is the revealing of the fourth card to the table. The turn is followed by another round of betting that follows the same formula detailed in part iii. Remember, this is No Limit Hold ‘Em, and players may risk any amount of the mBTC which they have accrued throughout the course of the game. When the hand has been checked down or all bets/raises matched the hand moves on to the “river”.<br><br>
        The river card is the fifth and final card revealed to the table. It’s revealing begins a final round of betting. The players who remain to contest the hand after all betting then have their cards revealed and the high hand wins the pot. In the event of a draw (equal hands held by two or more players) the pot is split accordingly.<br><br>
        After the conclusion of a hand, the dealer chip shifts clockwise to the left (the former small blind will now be the dealer) and the process repeats.

  </p></li>
  <li><p><em>Limit Texas Hold 'Em (LHE)</em>Limit Hold ‘Em has the same process of play as that of No Limit. The only difference is that there is a cap or maximum bet established that no player can exceed at any point during a game. The maximum bet is identified in the description of each table in the Synergy Poker Lobby. Limit Hold ‘Em is a great way to be introduced to poker, fewer all-in bets and a more conservative, but equally savvy style of play mean a great way to stay involved and improve one’s skill.</p></li>
