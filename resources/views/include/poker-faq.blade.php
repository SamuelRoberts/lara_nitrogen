                    <h4>General Poker FAQ</h4>
                    <strong>Why should I play poker at SynergySports?</strong>
                    <br><br>
                    We're incredibly secure, and enforce a strict anti-collusion policy. We also offer superior support and a rewarding loyalty program. If you have any questions about playing poker at Synergy, please submit a support ticket (accessible through the Synergy menu: Synergy > Support). Our customer service team would be happy to help!
                    <br><br><br>
                    <strong>I would like to play under different names. Am I able to create more than one poker account?</strong>
                    <br><br>
                    No. For reasons of security and to maintain an ethic of fair play, SynergySports prohibits the use of secondary or multiple accounts by the same user engaged in poker games.
                    <br><br><br>
                    <strong>I lost my password, am I able to recover it?</strong>
                    <br><br>
                    Yes. Submitting a support ticket (accessible through the Synergy menu: Synergy > Support) requesting a password reset will place you in contact with our customer service staff who will be happy to lead you through the process of recovering your account.
                    <br><br><br>
                    <strong>Am I able to transfer funds between my sportsbook and poker accounts?</strong>
                    <br><br>
                    As a user of SynergySports, your sportsbook and poker accounts are one and the same. Funds from your account may be distributed for wagering with our sportsbook or entering poker games.
                    <br><br><br>
                    <strong>How do I know how much money is in the pot?</strong>
                    <br><br>
                    The total amount of money in the pot on a given hand will be displayed for all players to see on the table next to the cards that have been revealed.
                    <br><br><br>
                    <strong>Can I play at the same table as my friend?</strong>
                    <br><br>
                    Yes, of course you can! Playing with, and trash-talking to a friend can make a great game of poker even more enjoyable. Collusion of any kind is, however, strictly prohibited (see poker rules) and any users discovered to be cheating through this method will be banned from the site and their funds frozen pending investigation.
                    <br><br><br>
                    <strong>What is a limit game?</strong>
                    <br><br>
                    A limit game places a cap on the amount of money a player may risk or wager on a particular hand. SynergySports offers both limit and no limit poker games.
                    <br><br><br>
                    <strong>Am I able to change my Avatar?</strong>
                    <br><br>
                    Yes! You are able to change your Synergy Poker Avatar by selecting your poker settings (the small gear icon beside the "Lobby" tab near the top left of your browser), then selecting "AVATARS" in the settings menu.
                    <br><br><br>
                    <strong>What happens if I am disconnected in the middle of a game?</strong>
                    <br><br>
                    If you lose your connection to poker, your disconnection timebank will take effect. If you reconnect before your disconnection timebank expires, you will resume playing the hand, otherwise your hand will be folded. The disconnection timebank starts at 60 seconds and will shrink if you become disconnected.
                    <br><br><br>
                    <strong>What happens if the poker server has an issue during a cash game or tournament?</strong>
                    <br><br>
                    If the poker server crashes during a cash game, the hand will be invalidated and the stakes returned to each player. In a tournament, the tournament will end and remaining players will split the prize pool.
                    <br><br><br>
                    <strong>What are the minimum stakes?</strong>
                    <br><br>
                    0.01/0.02 mBTC
                    <br><br><br>
                    <strong>What is the rake?</strong>
                    <br><br>
                    Rake is the small amount of mBTC SynergySports collects from each hand that goes to flop.

                    Generous rakeback bonuses are also offered through the Synergy Rewards program. Collect Nitro and use them to purchase bitcoin or free bets.<br><br>

                    Full rake tables can be found under the poker menu: Poker Rules -> Rake<br><br>

                    <br><br><br>
                    <strong>How can you ensure the integrity of your games?</strong>
                    <br><br>
                    At SynergySports, we use a standard <a href="http://en.wikipedia.org/wiki/Mersenne_twister" target="_blank">Mersenne Twister</a> for RNG and <a href="http://en.wikipedia.org/wiki/Fisher%E2%80%93Yates_shuffle" target="_blank">Fisher-Yates shuffle</a> for shuffling cards prior to each deal.
                    <br><br><br>
                    <strong>Am I able to be active at more than one table at a time?</strong>
                    <br><br>
                    Yes. Users are able to play at multiple tables at the same time. Remember to be active only in as many as you feel confident in managing! Within the poker client, you can choose to open tables in a new window. See the "Game Settings" area by clicking the icon in the top left corner of the poker client.
                    <br><br><br>
                    <strong>Is there a poker lobby chat available?</strong>
                    <br><br>
                    There is no separate lobby chat for Synergy Poker; however, our platform allows users to maintain access to the main chat feature of our site so you can keep chatting with your friends as you’re winning your poker game!
                    <br><br><br>
                    <strong>Does the poker software allow me to muck the winning poker hand?</strong>
                    <br><br>
                    Yes, you can change the default for this option in the poker settings.
                    <br><br><br>
                    <strong>How do I request a hand history?</strong>
                    <br><br>
                    Hand histories are available in the client while playing, however they will not save to your computer automatically at this time.
                    <br><br><br>
                    <strong>How do I request my poker play statistics?</strong>
                    <br><br>
                    Your poker statistics are not available upon request at this point in time.
                    <br><br><br>
                    <strong>Do you offer notes on other players? How can I use this?</strong>
                    <br><br>
                    Synergy Poker currently does not offer notes on competing users, however you’re more than welcome to play it savvy and keep notes of your own!
                    <br><br><br>
                    <strong>I find the Avatars at the table distracting, can I turn them off?</strong>
                    <br><br>
                    Unfortunately not at this time. User Avatars will always appear at the table once they have been selected.
                    <br><br><br>
                    <strong>I am faced with a difficult decision in a poker tournament, may I “request time”?</strong>
                    <br><br>
                    Yes, there is an extended timebank which you can use. After half of your normal timebank has expired, press "request time" and you will be given access to your extended timebank. The extended timebank starts at 30 seconds and shrinks as you use it. All users must play their hands in the given time with no exceptions.
                    <br><br><br>
                    <strong>What happens if a tournament needs to be cancelled?</strong>
                    <br><br>
                    If a tournament is cancelled for whatever reason, all your BTC that was placed down upon registration is refunded to your account.
                    <br><br><br>
                    <h4>Loyalty Program FAQ</h4>
                    <strong>What is a Loyalty Point?</strong>
                    <br><br>
                    Loyalty Points determine your Synergy Rewards level for the following month. The more Loyalty Points you accumulate, the higher level you’ll have. You can earn points by playing poker for bitcoin. Loyalty Points reset on the first day of each calendar month.
                    <br><br><br>
                    <strong>How do I earn Loyalty Points?</strong>
                    <br><br>
                    You earn points based on the rake you contribute to hands of poker. Loyalty Points will be 2.5 times the amount raked.
                    <br><br><br>
                    <strong>What are Nitro?</strong>
                    <br><br>
                    Nitro have value and can be exchanged for bitcoin or free bets. The higher your Synergy Rewards level, the more Nitro you receive!
                    <br><br><br>
                    <strong>Monthly Synergy Rewards and Nitro levels:</strong>
                    <br><br>
                    Please see the Poker Rewards section using the menu on the left.
                    <br><br><br>
                    <h4>Play Money FAQ</h4>
                    <strong>How much PLY do I start with?</strong>
                    <br><br>
                    Every user account will be topped up to 1PLY upon entering Synergy Poker for the first time.
                    <br><br><br>
                    <strong>What if I run out of PLY?</strong>
                    <br><br>
                    Each user is able to top up once every 2 hours when you balance is 0.99 or less.
                    <br><br><br>
                    <strong>Can I buy PLY with real btc??</strong>
                    <br><br>
                    No. If you are looking to continue playing poker, and have the BTC to spend, try one of our exciting low buy-in tables where users play for mBTC!
                    <br><br><br>
                    <strong>Are there PLY transfers between players?</strong>
                    <br><br>
                    PLY is credited to user accounts on an individual basis and remains in that account solely for use by that credited individual.
