						<strong>Update August 4th, 2015: New Jackpot Prize Allocation</strong><br>
						Starting this month, we’ve updated the Parlay Jackpot prize allotment. Contenders in our Middleweight and Featherweight pools will now be playing for a larger share of the monthly jackpot!<br><br>
						New Jackpot Share Structure:
						<ul><li>Heavyweight - 60% of awarded jackpot</li>
							<li>Middleweight - 30% of awarded jackpot</li>
							<li>Featherweight - 10% of awarded jackpot</li></ul>
							<br>				                                
							<strong>The NEW Parlay Jackpot!</strong><br>
							We’re sprucing up the Parlay Jackpot in a big way! The new jackpot promotion features multiple prize pools to suit everyone’s betting style. Now, you can win a share of the jackpot while playing your normal stakes. The new Parlay Jackpot still carries the same excitement and huge jackpots, but with multiple prize pools, there are more chances to win! 
							<br><br>
							<strong>What is the Parlay Jackpot?</strong><br>
							The Parlay Jackpot is a prize pool which we award to a number of our members every month. There are now 3 pools. The top 7 players in each pool (21 total) with the highest parlay payouts win a share of the prize. Parlays that lose by only one leg can still count towards the jackpot prize!
							<br><br>
							<strong>So what’s new?</strong><br>
							The parlay jackpot now features multiple prize pools as opposed to the previous single prize pool. Each of the three pools will have a set portion of the jackpot allocated to its winners. The pools have different maximum wager sizes and allocated jackpot winnings.
							<br><br>
							<strong>What are the prize pools?</strong><br>
							Heavyweight - Unlimited max wager - Winners take 60% of the awarded jackpot*<br>
							Middleweight - Max 0.1 BTC wager - Winners take 30% of the awarded jackpot*<br>
							Featherweight - Max 0.01 BTC wager - Winners take 10% of the awarded jackpot*<br>
							<br>
							*80% of the total jackpot will be awarded at the end of each month as laid out in the leaderboard. 20% of the jackpot will be used to seed the next month's jackpot. 
							<br><br>
							<strong>What are the general rules? </strong><br>
							(1) Your parlay must contain at least 2 winning plays (parlays that get reduced and only end up with one winning play will not count)<br>
							(2) Your parlay must not contain more than 1 loss, push or cancelled play (parlays that have one push and one cancel for example will not count) <br>
							(3) All legs of the parlay must take place within the same calendar month; if a match takes place in the next month, your parlay will not count towards the promotion.<br>
							(4) Only one account per household is eligible to win<br>
							(5) All prizes for this promotion are awarded into the winners' SynergySports account on or before the 10th of the following month.<br>
							(6) You may not use parlay wagers to increase your exposure to single game outcomes more than is allowed via a maximum straight bet. Redundant parlay wagers made with the intention of circumventing single game betting limits will be canceled. Redundant parlays made to circumvent the maximum payout limits will be canceled as well. 
							<br><br>
							<strong>What are the specified rules for Heavyweights?</strong><br>
							(1) No max bet<br>
							(2) 60% of the awarded jackpot will be paid to the top 7 players on the Heavyweight leaderboard
							<br><br>
							<strong>What are the specified rules for Middleweights?</strong><br>
							(1) Max bet is 0.1 BTC<br>
							(2) All Middleweights eligible bets are by default eligible for the Heavyweight pool as well.<br>
							(3) 30% of the awarded jackpot will be paid to the top 7 players on the Middleweight leaderboard
							<br><br>
							<strong>What are the specified rules for Featherweights?</strong><br>
							(1) Max bet is 0.01 BTC<br>
							(2) All Featherweight eligible bets are by default eligible for the Middleweight and Heavyweight pools.<br>
							(3) 10% of the awarded jackpot will be paid to the top 7 players on the Featherweight leaderboard
							<br><br>
							<strong>Can I win more than one pool with a single parlay?</strong><br>
							Yes! There are no restrictions on minimum bet size for each pool. Therefore, you could win in more than one pool with a single parlay depending on the winning odds and risked amount. For example, a Middleweight sized parlay (0.1 BTC max wager) could win in both the Heavyweight and Middleweight pools if its parlay payout ranked within the top 7 in both pools. However, it would not qualify for the Featherweight pool because its wager amount is too high. Featherweight sized wagers (0.01 BTC and under) are the only parlays which have the potential to win a share of all three prize pools.
							<br><br>
							<strong>How do I win the jackpot(s)? </strong><br>
							The users that have their highest parlay payout (rounded to the nearest 0.1 BTC), ranked second by highest odds, listed in the leaderboards each month will win the corresponding prize(s). As mentioned above, Featherweight or Middleweight sized parlays have the ability to qualify for more than one prize pool!
							<br><br>
							<strong>What makes the jackpot grow? </strong><br>
							Every time a parlay wager is placed on SynergySports, a small percent of that amount is added to the jackpot amount. Each month, the jackpot is initially seeded with 20% of the previous month’s jackpot.
							<br><br>
							<strong>How are the odds for qualifying parlays calculated?</strong><br>
							The odds for a parlay will be calculated using the winning legs only. Parlays which have a single losing leg will still be eligible for the promotion (as before), however the odds will only use the winning leg portion of the parlay. The payout amount will still use the original amount. This allows parlays which are winners in all legs to have a greater chance in the contest.
							<br><br>
							<strong>When does this promotion start / end? </strong><br>
							The promotion starts on the 1st of every month at 00:00 UTC. The end date of the promotion is the last day of each month, 23:59 UTC. For a parlay to count, it must be submitted before the 00:00 UTC and no game can be delayed more than 24 hours. All legs of the parlay must take place within the same calendar month; if a match takes place in the next month, your parlay will not count towards the promotion.
							<br><br>
							<strong>When are the prizes awarded? </strong><br>
							All prizes for this promotion are awarded into the winners' SynergySports account on or before the 10th of the following month. 
							<br><br>
							<strong>Why don't I see my parlay on the leaderboard? </strong><br>
							Parlays will only show up if all legs of the parlay are graded. Only a user's highest parlay counts towards the leaderboard. Users are only eligible for one spot on each individual leaderboard.  However one wager can be eligible for a spot on each leaderboard if it qualifies under the Max wager rules outlined above.  
							<br><br>
							<strong>What happens in the case of a tie? </strong><br>
							Instead of splitting the prize for tied parlay winners, payouts will be sorted first by payout and then second by odds. In the case of a tie, the user with higher odds on their betslip will be ranked and rewarded higher than the user with reduced odds. In the case of the payout and the odds being identical, players will then split the prize. 
							<br><br>
							<strong>What is the limit on parlays?</strong><br>
							The global parlay limit is 50 BTC. Please note that not all sports and events are included in the global limit. If your slip includes certain eSports, Tennis or other smaller or less popular leagues, your parlay may limit the maximum payout to less than the global limit. 
							<br><br>
							<strong>Can I hide my betslips from the promotion and still win?</strong><br>
							We always strive for a fair balance between privacy and transparency. In order to maintain this, users who wish to participate in the parlay promotion will no longer be allowed to hide their betslips. If you would like to have your betslips remain private, you do have the option to withdraw yourself from this monthly promotion. 
							<br><br>
							<strong>I don't like the rules or I have a suggestion to improve this promotion. </strong><br>
							We are always open to new ideas! Catch us in chat, or create a support ticket with your feedback - that way we can keep making our promotions better and better! 
