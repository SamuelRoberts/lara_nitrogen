                   <strong>What is a betting exchange?</strong>
                   <br><br>
                   Using Synergy’s Sportsbook, players place bets against the house. We collect money for all the outcomes of a certain event, and pay out the winning players.
                   <br><br>
                   Unlike a sportsbook, an exchange allows players to wager against each other. Every bet has a winning player and a losing player. The loser’s stake pays the winner’s profit, and SynergySports will keep a small commission from every winning bet.
                   <br><br><br>

                   <strong>What are the benefits of an exchange?</strong>
                   <br><br>

                   <ul>
                           <li>You are able to set your own odds for a match</li>
                           <li>there are no limits; you can wager as much as you like</li>
                   </ul>

                   <br><br>

                   <strong>How is placing a bet on an exchange different from a traditional sportsbook?</strong>
                   <br><br>
                   On Synergy’s Sportsbook, you are always betting on a certain outcome to win.
                   <br><br>
                   On Synergy’s Exchange, you can make two kinds of bets:
                   <br><br>

                   <ul>
                           <li>a back bet (you want the outcome to win)</li>
                           <li>a lay bet (you want the outcome to lose)</li>
                   </ul>

                   <br>
                   Wagering on a back bet is similar to how our sportsbook works. The difference is that on an exchange, each back bet will need a matching lay bet to have action.
                   <br><br>
                   Whenever you place a bet (back or lay), you will be automatically matched if the opposite side exists at the odds you requested. For partial matches, any remaining wager amount will remain on the market until someone else matches this amount.
                   <br><br><br>

                   <strong>How do I actually place a bet?</strong>
                   <br><br>
                   When placing a bet on NBA money lines, you will be presented with two teams. Each team will have the option to back or lay.
                   <br><br>
                   Back buttons will always be blue, and lay buttons will always be pink.
                   <br><br>
                   The away team is listed on the left, and the left set of buttons will correspond to that team. The home team is listed on the right, and the right set of buttons will correspond to that team.
                   <br><br>
                   When you press a back or lay button, your choice will show up on your betslip. Be sure to note if this is a back bet or lay bet - the same blue and pink colours are used.
                   <br><br>
                   The odds will be filled in with the current offer. If no offer is present, we will suggest the starting odds. At this point, you can edit the odds and the risk amount - we have not set limits on these amounts.
                   <br><br>
                   When betting “back”:
                   <ul>
                           <li>the risk amount is what you want to risk</li>
                           <li>the “to win” is what you want to win</li>
                           <li>the odds represent the odds you want on the match</li>
                   </ul>

                   <br>
                   When betting “lay”:
                   <ul>
                           <li>the risk amount is what you will let someone else bet on the match</li>
                           <li>the “to win” is what you will promise to pay them if they win</li>
                           <li>the odds are the odds you are offering to the backer</li>
                   </ul>

                   <br>
                   Once this is done, you can decide when this bet will expire. By default, the bet will be set to close when we expect the game to start. You can edit this time by any amount. For example, if you were willing to let it get matched 10 minutes after the expected start time, you could push this time out by 10 minutes and your offer will remain open until then. The other option is to select “User Managed” which allows you to close the bet yourself. THe system will not cancel the wager for you until the game is over. This option allows for live wagering, as you can create an accept offers anytime during the match.
                   <br><br>
                   Once you’re happy with the betslip, press the “Place” button, followed by “Confirm”. If there are no errors, your betslip will be submitted.
                   <br><br>
                   When you place your bet on Synergy’s Exchange, it will either be matched right away, partial matched, or sit unmatched until someone decides to bet the other side. You can view both your matched and unmatched wagers on the “Open Offers” tab of your betslip. Once the match is graded, the bet will be moved to the “My Exchange Wagers” page under the Exchange menu items.
                   <br><br><br>

                   <strong>If I am betting against other players, how do I know I’ll be paid?</strong>
                   <br><br>
                   We hold 100% of the funds you bet while an offer is on the table. This means that if you see an offer on the Exchange for any amount, you can be sure the amount is guaranteed.
                   <br><br>

                   <strong>I don’t want people to know what I am betting on, or that I am backing or laying against them.</strong>
                   <br><br>
                   As with the Sportsbook, all exchange wagers are 100% anonymous. You will have the option to share your exchange betslip on chat or other mediums with a unique link, but you certainly don’t have to!
                   <br><br><br>

                   <strong>How much does the exchange take as a percentage?</strong>
                   <br><br>
                   SynergySports' Exchange takes a percentage off of the amount won only (it is NOT calculated on the stake plus winnings). At launch, this will be 2% of the won amount across all markets. This will change once we launch our loyalty program.
                   <br><br>
                   We will gradually refine our commission structure to be market-specific, and to include commission discounts based on our loyalty program. All accounts that place wagers prior to the release of the loyalty program will be retroactively credited their discount.
                   <br><br><br>

                   <strong>Does this percentage cost me more or less than using a sportsbook to place bets?</strong>
                   <br><br>
                   It depends on the sportsbook's juice!
                   <br><br>
                   A sportsbook line of 1.91/1.91 (-110 / -110) would keep $10 for every $220 wagered, or 4.55%.
                   <br><br>
                   The exchange line of 2.0/2.0 (+100/+100) would keep $2.20 for every $220 wagered, or 1%
                   (on the exchange with a 2% commission market, using the no-vig line).
