					<strong>When does this promotion end?</strong>
					<br>
					The NFL Survivor Pool will end after the completion of the regular season. Individual pools may end earlier if all participants are eliminated before the end of the season.
					<br><br>
					<strong>What does the Guaranteed Prize mean?</strong>
					<br>
					The Guaranteed Prize means this is the minimum we will award for this pool. If the number of entrants is large enough, then the pool will be larger than the guaranteed amount! 100% of all entries go towards the pool. SynergySports takes zero (0%) fees.
					<br><br>
					<strong>When are the prizes awarded to the winners?</strong>
					<br>
					All prizes for this promotion are awarded into the winners' SynergySports account by January 13, 2016. In the event of the survivor pool ending earlier (all participants eliminated before the end of the regular season), prizes will be awarded ahead of schedule for any completed/finished survivor pool.
					<br><br>
					<strong>What if multiple people win a pool?</strong>
					<br>
					If multiple people win a pool, the total prize of the pool will be split between them.
					<br><br>
					<strong>Are there any rollover requirements for this promotion?</strong>
					<br>
					No. You are free to instantly withdraw any prizes you win from this promotion.
					<br><br>
					<strong>Can I enter more than once?</strong>
					<br>
					You can absolutely buy into the paid pools more than once! However, you can only submit one entry into the free pool.
					<br><br>
					<strong>Can I create multiple accounts to enter the free pool more than once?</strong>
					<br>
					No, you cannot enter the free pool with multiple accounts. We have extremely sophisticated methods for detecting multiple accounts in our system. Any user or team of users that attempt to defraud the contest by using more than one account will forfeit all promotion prizes and be banned from the contest. Any prizes won from fraud accounts will be split among all remaining prize winners.
					<br><br>
					<strong>Is SynergySports taking a hosting fee for this contest?</strong>
					<br>
					No; we’re not taking a percentage of the pots! 100% of all prizes and entry fees is returned to the participating users!
					<br><br>
					<strong>Can I see other user's picks?</strong>
					<br>
					Yes. After the first game on each Sunday has started and all picks are locked in, you will be able to see who has picked what team for that week!
					<br><br>
					<strong>What if the game ends in a tie?</strong>
					<br>
					All games that end in a tie will be counted as a loss and will end your survivor entry.
					<br><br>
					<strong>What if the team I selected has a bye this week?</strong>
					<br>
					If you select a team that has a bye, it will count as a loss. You must select a winning team each and every week in order to survive to the following week. You’re responsible for knowing what teams are playing each week when you make your selection.
					<br><br>
					<strong>Can I submit picks in advance?</strong>
					<br>
					Yes, you can select your entire survivor picks right away if you like.
					<br><br>
					<strong>I forgot to submit my pick this week; can I still move on?</strong>
					<br>
					Unfortunately no, only winning picks will allow you to move forward to the next round. If you forget to submit a pick for a certain week, that will mark the end of your pool entry.
					<br><br>
					<strong>I didn't forget to submit my pick, but it wouldn't let me place the pick because it says the game has started even though it hasn't started yet!</strong>
					<br>
					We use the scheduled time for the first game each week. We do not use the kickoff time. All picks that fail to be submitted before the scheduled time will not count and the system will not allow them to be processed. Do not leave your picks until the last minute. No refunds will be granted, no picks will be entered by customer support, no exceptions will be made.
					<br><br>
					<strong>I've changed my mind, can you refund my survivor pool entry?</strong>
					<br>
					No. All entries are final and will not be refunded. No exceptions will be made.
					<br><br>
					<strong>I made a mistake with a pick. Can I change it?</strong>
					<br>
					No. All pick submissions are final. Nobody will be allowed to change a submitted pick. No refunds will be granted, no picks will be changed by customer support, no exceptions will be made.
					<br><br>
					<strong>But the mistake makes me lose my pool, I didn't realize team X didn't play in week Y. Can you just let me choose another team?</strong>
					<br>
					No. Your survivor pool picks are final. No refunds will be granted, no picks will be entered by customer support, no exceptions will be made.
					<br><br>
					<strong>What if something happens that is not covered by any of the rules above?</strong>
					<br>
					In the case where no rule can be applied, SynergySports reserves the right to apply their sole judgement to handling the issue. In the case where the judgement can/will affect multiple users, all decisions will remain consistent across all user accounts.

