					<strong>When are my picks due?</strong>
					<br>
					Each pick must be selected prior to the scheduled time of the first game every Sunday (typically @ 17:00 UTC every Sunday). For picks on the Thursday night games, those must be submitted prior to the start of the scheduled time for every Thursday game.
